'use strict';


/**
 * Trim the given string, and replace all the sub-sequence of 1 or several space-like characters by a single space.
 */
exports.trimAndCollapseSpaces = function(text) {
	return text.replace(/^\s+|\s+$/g, '').replace(/\s+/g, ' ');
};
